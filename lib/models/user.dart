class User {
  String uid;
  String userName;
  String fullName;
  String birthday;
  String email;
  String photoURL;

  User(String uid, String userName, String fullName, String birthday, String email, String photoURL) {
    this.uid = uid;
    this.userName = userName;
    this.fullName = fullName;
    this.birthday = birthday;
    this.email = email;
    this.photoURL = photoURL;
  }
}
